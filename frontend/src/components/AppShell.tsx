const NAV_ITEMS: NavItem[] = [
  { to: '/app/dashboard', label: 'Dashboard',  icon: <IconDashboard /> },
  { to: '/app/billing',   label: 'Billing',    icon: <IconBilling /> },
  { to: '/app/matters',   label: 'Matters',    icon: <IconMatters /> },
  { to: '/app/clients',   label: 'Clients',    icon: <IconClients /> },
  { to: '/app/discovery', label: 'Discovery',  icon: <IconDiscovery /> },
  { to: '/app/admin',     label: 'Admin',      icon: <IconAdmin />, roles: ['admin'] },
]

export default function AppShell() {
  const { profile, signOut } = useAuth()
  const navigate = useNavigate()
  const [sidebarOpen, setSidebarOpen] = useState(false)

  const visibleItems = NAV_ITEMS.filter(
    item => !item.roles || (profile && item.roles.includes(profile.role))
  )

  async function handleSignOut() {
    await signOut()
    navigate('/login', { replace: true })
  }

  const navLinkClass = ({ isActive }: { isActive: boolean }) =>
    `flex items-center gap-3 px-3 py-2.5 rounded-lg text-sm font-medium transition-colors ${
      isActive
        ? 'bg-navy text-white'
        : 'text-text-secondary hover:bg-navy/8 hover:text-navy'
    }`

  const sidebar = (
    <aside className="flex flex-col h-full w-56 bg-off-white border-r border-border">
      {/* Brand */}
      <div className="px-4 h-16 flex items-center border-b border-border">
        <span className="font-display text-xl text-navy tracking-tight">Cyclone</span>
      </div>

      {/* Nav */}
      <nav className="flex-1 overflow-y-auto px-3 py-4 space-y-1">
        {visibleItems.map(item => (
          <NavLink key={item.to} to={item.to} className={navLinkClass}
            onClick={() => setSidebarOpen(false)}>
            {item.icon}
            {item.label}
          </NavLink>
        ))}
      </nav>

      {/* User footer */}
      <div className="px-4 py-4 border-t border-border">
        <div className="text-xs text-text-secondary mb-2 uppercase tracking-widest font-semibold">
          {profile?.role}
        </div>
        <button
          onClick={handleSignOut}
          className="w-full flex items-center gap-2 text-sm text-text-secondary
                     hover:text-navy transition-colors"
        >
          <svg className="w-4 h-4" fill="none" stroke="currentColor" strokeWidth={1.5} viewBox="0 0 24 24">
            <path strokeLinecap="round" strokeLinejoin="round"
              d="M15.75 9V5.25A2.25 2.25 0 0013.5 3h-6a2.25 2.25 0 00-2.25 2.25v13.5A2.25 2.25 0 007.5 21h6a2.25 2.25 0 002.25-2.25V15M12 9l-3 3m0 0l3 3m-3-3h12.75" />
          </svg>
          Sign out
        </button>
      </div>
    </aside>
  )

  return (
    <div className="flex h-screen overflow-hidden bg-white">
      {/* Desktop sidebar */}
      <div className="hidden md:flex flex-shrink-0">{sidebar}</div>

      {/* Mobile sidebar overlay */}
      {sidebarOpen && (
        <div className="md:hidden fixed inset-0 z-40 flex">
          <div className="fixed inset-0 bg-black/30" onClick={() => setSidebarOpen(false)} />
          <div className="relative z-50 flex">{sidebar}</div>
        </div>
      )}

      {/* Main area */}
      <div className="flex flex-col flex-1 min-w-0 overflow-hidden">
        {/* Mobile top bar */}
        <header className="md:hidden flex items-center gap-4 px-4 h-16 border-b border-border bg-white">
          <button
            onClick={() => setSidebarOpen(true)}
            className="p-2 rounded-md text-text-secondary hover:text-navy"
          >
            <svg className="w-5 h-5" fill="none" stroke="currentColor" strokeWidth={1.5} viewBox="0 0 24 24">
              <path strokeLinecap="round" strokeLinejoin="round" d="M3.75 6.75h16.5M3.75 12h16.5m-16.5 5.25h16.5" />
            </svg>
          </button>
          <span className="font-display text-xl text-navy tracking-tight">Cyclone</span>
        </header>

        {/* Page content */}
        <main className="flex-1 overflow-y-auto">
          <Outlet />
        </main>
      </div>
    </div>
  )
}
